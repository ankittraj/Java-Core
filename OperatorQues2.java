class OperatorQues2{
    static public void main(String [] s){
        int a;
        a=7/2;
        System.out.println(a); 
        a=-7/2;
        System.out.println(a); 
        a=7/-2;
        System.out.println(a);
        a=-7/-2;
        System.out.println(a);  

        a=7%2;
        System.out.println(a); 
        a=-7%2;
        System.out.println(a); 
        a=7%-2;
        System.out.println(a);
        a=-7%-2;
        System.out.println(a);  
    }
}